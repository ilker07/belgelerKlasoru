void ST7789_RamWrite(uint16_t *pBuff, uint16_t Len)
{
  printf("RamWrite Fonksiyonu *pBuff: %d \n",*pBuff);
  while (Len--)
  {
  	printf("Data yollama *pBuff >> 8: %d \n",*pBuff >> 8);
    ST7789_SendData(*pBuff >> 8);  
    printf("Data yollama *pBuff & 0xFF: %d \n",*pBuff & 0xFF);
    ST7789_SendData(*pBuff & 0xFF);
  }  
}

